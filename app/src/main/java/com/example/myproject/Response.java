package com.example.myproject;

public class Response {

    private String ObjectId;
    private String FacilityId;
    private String ComplaintCentre;
    private String ContactInf;
    private String DivCode;
    private String DivName;
    private String Name;
    private String ComPhoneNo;

    public String getObjectId() {
        return ObjectId;
    }

    public void setObjectId(String objectId) {
        ObjectId = objectId;
    }

    public String getFacilityId() {
        return FacilityId;
    }

    public void setFacilityId(String facilityId) {
        FacilityId = facilityId;
    }

    public String getComplaintCentre() {
        return ComplaintCentre;
    }

    public void setComplaintCentre(String complaintCentre) {
        ComplaintCentre = complaintCentre;
    }

    public String getContactInf() {
        return ContactInf;
    }

    public void setContactInf(String contactInf) {
        ContactInf = contactInf;
    }

    public String getDivCode() {
        return DivCode;
    }

    public void setDivCode(String divCode) {
        DivCode = divCode;
    }

    public String getDivName() {
        return DivName;
    }

    public void setDivName(String divName) {
        DivName = divName;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getComPhoneNo() {
        return ComPhoneNo;
    }

    public void setComPhoneNo(String comPhoneNo) {
        ComPhoneNo = comPhoneNo;
    }
}
